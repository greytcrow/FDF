#include "../includes/fdf.h"

void	ft_init_matrix(t_matrix *a)
{
	int		i;
	int		j;

	i = -1;
	j = -1;
	while (++i < 3)
	{
		while (++j < 3)
			a->matrix[i][j] = 0;
		j = -1;
	}
}
